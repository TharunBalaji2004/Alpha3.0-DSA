public class ifelse {
    public static void main(String[] args) {
        int age = 20;

        if (age >= 18){
            // Executes when the condition is True
            System.out.println("You can vote!");
        } else {
            // Executes when the condition is False
            System.out.println("You can't vote!");
        }
    }
}

