public class output {
    public static void main(String[] args) {
        // print() - prints the output string
        // println() - prints the output string and newline
        System.out.println("Hello there!");
        System.out.println("Hello world!");
        
        // \n - Newline character \t - Tabspace character
        System.out.print("Hello\tThere"); 
    }
}
