public class logical {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;

        boolean st1 = a>b;
        boolean st2 = a<b;

        System.out.println(st1 && st2);
        System.out.println(st1 || st2);
        System.out.println(!st1);
        System.out.println(!st2);
    }
}

/*
    LOGICAL OPERATORS:

    && - Logical AND
    || - Logical OR
    ! - Logical NOT
 */